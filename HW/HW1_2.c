
#include <stdio.h>

int main(){
    
    //拜D1.2
    float input01 , input02;                                              //脓iㄢBI计
    printf ( "Question 1.2\n" );
    printf ( "----------------------\n" );
    printf ( "叫块Jㄢ蛹啤G\n" );                          
    scanf  ( "%f" , &input01 );                                           //弄JBI计@
    scanf  ( "%f" , &input02 );                                           //弄JBI计G
    printf ( "ㄢ计邾[啊G%f\n" , (input01 + input02) );                  //块Xㄢ计邾[
    printf ( "ㄢ计鄞瞵啊G%f\n" , (input01 - input02) );                  //块Xㄢ计鄞
    printf ( "ㄢ计郗棘啊G%f\n" , (input01 * input02) );                  //块Xㄢ计郗
    printf ( "ㄢ计郯，啊G%f\n" , (input01 / input02) );                  //块Xㄢ计郯
    printf ( "----------------------\n" );
    printf ( "\n" );

    return 0;
}
